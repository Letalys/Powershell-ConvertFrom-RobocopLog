                        $ModifiedFileExists = $Log.Files.Modified | Where-Object {
                            $_.FilePath -eq $filePath -and
                            $_.FileSize -eq $fileSizeRaw -and
                            $_.Timestamp -eq $fileTimestamp
                        }

