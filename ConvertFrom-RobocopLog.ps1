                            FilePath   = $filePath    
                            FileSize   = $fileSizeRaw  
                            Timestamp  = $fileTimestamp 
                        }
                    }

                    # Gestion des fichiers identiques (SameFiles) avec récupération de la taille et de l'horodatage si présent
