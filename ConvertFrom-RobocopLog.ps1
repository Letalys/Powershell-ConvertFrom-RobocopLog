                                FilePath   = $filePath    
                                FileSize   = $fileSizeRaw  
                                Timestamp  = $fileTimestamp 
                            }
                        }else{
                            Write-Warning "$($Culture.WarningModifiedFileExist) : $($ModifiedFileExists.FilePath)"
                        }
                    }

