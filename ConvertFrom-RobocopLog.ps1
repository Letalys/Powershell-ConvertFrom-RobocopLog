                            # Adding the file to the list of new files (NewFiles) with its attributes
                            $Log.Files.New += [pscustomobject]@{
                                FilePath   = $filePath    
                                FileSize   = $fileSizeRaw  
                                Timestamp  = $fileTimestamp 
